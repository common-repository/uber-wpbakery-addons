/*
=================================================================================================================
Function uber_wpb_vc_font_icons_enqueue() - enqueue icon fonts for VC elements
=================================================================================================================
*/
function uber_wpb_vc_font_icons_enqueue( $font ){
	
	$icon_suffix = '';

	if( $font ){
		if( strpos($font, 'uberwpb-') !== false ){
			
			$xplode = explode( '-', $font );
			$font_class = $xplode[0].'-'.$xplode[1];
			$url = UBER_WPB_PLUGIN_URI . '/fonts/icons/' . $xplode[1] . '/style.css';
			wp_enqueue_style( $font_class, $url);
		}
		elseif ( strpos( $font, 'pe-7s' ) !== false){
			wp_enqueue_style( 'pe-7s', UBER_WPB_PLUGIN_URI.'fonts/icons/pe-7s/style.css' );
		}
		else{
			wp_enqueue_style( 'pe-7f', UBER_WPB_PLUGIN_URI.'fonts/icons/pe-7f/style.css' );
		}
	}
}